# Python数据类型：

# 数字类型
# 整形（int）：表示整数
int_num = 123

# 浮点型（float）：表示小数
float_num = 123.45

# 复数（complex）：表示实部和虚部的数，格式为a + bj，其中a是实部，b是虚部，j是虚数单位
complex_num = 1 + 2j

# 布尔类型（bool）：表示真假，可由其他类型转换
bool_true = True
bool_false = False

# 字符串类型
# 字符串（str）：表示文本数据
str_num = "Hello, World!"
# 字节类型（bytes）：表示二进制数据，常用于网络通信和文件操作
bytes_num = b"Hello, World!"

# 序列类型
# 列表（list）：表示有序的可变集合，可以包含不同类型的元素
list_num = [1, 2, 3, 4, 5]
list_mixed = [1, "Hello", 3.14, True]

# 元组（tuple）：与列表类似，但不可变，即创建后不能修改
tuple_num = (1, 2, 3, 4, 5)

# 字典（dict）：表示键值对的无序集合
dict_num = {"one": 1, "two": 2, "three": 3}

# 集合（set）:表示无序的唯一元素集合
set_num = {1, 2, 3, 4, 5}
